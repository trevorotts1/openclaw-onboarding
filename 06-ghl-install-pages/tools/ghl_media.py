def image_tag(cdn_url: str, alt: str = "") -> str:
    """Return the ``<img>`` snippet that references the PUBLIC CDN URL in-page.

    The ``cdn_url`` MUST be an ``http(s)`` URL (the public GCS object URL from
    ``upload_media``) — a ``file://`` or SVG-data placeholder is REFUSED, so a
    stub can never be spliced into the page. The caller passes the returned HTML
    to ``ghl_rest_canvas.edit_element_customcode`` (the in-browser autosave then
    lands it)."""
    _require(cdn_url, "cdn_url")
    low = cdn_url.strip().lower()
    if not (low.startswith("http://") or low.startswith("https://")):
        raise ValueError(
            f"cdn_url must be an http(s) URL (got {cdn_url!r}) — file:// / data: "
            "placeholders are refused; only the public GCS media URL is allowed"
        )
    alt_attr = alt.replace('"', "&quot;") if alt else ""
    return f'<img src="{cdn_url}" alt="{alt_attr}" loading="lazy">'


def build_image_manifest(records: list[dict], out_path: str | None = None) -> list[dict]:
    """Assemble + (optionally) write ``images/manifest.json``.

    Each input record maps one generated+uploaded image::

        {
            "id": "hero-soap-trio",
            "prompt": "<the full prompt incl. the English/Latin pin>",
            "file": "<RUN_DIR>/images/hero-soap-trio.png",
            "cdn_url": "https://storage.googleapis.com/msgsndr/...",  # PUBLIC, https
            "cdn_http_status": 200,                                   # verified 200
            "used_on_page_id": "PAGEID...",
            "file_id": "<ghl media fileId>"          # optional provenance
        }

    The contract is strict (per the spec): ``cdn_url`` MUST be ``https`` (no
    ``file://``, no placeholder note) and ``cdn_http_status`` MUST be 200. A record
    violating either is rejected LOUD — the manifest can only ever describe real,
    publicly-resolving images.

    Returns the validated list (and writes it to ``out_path`` when given).
    """
    if not isinstance(records, list) or not records:
        raise ValueError("records must be a non-empty list of image records")
    out: list[dict] = []
    for i, rec in enumerate(records):
        if not isinstance(rec, dict):
            raise ValueError(f"records[{i}] must be a dict")
        rid = str(rec.get("id", "")).strip()
        _require(rid, f"records[{i}].id")
        cdn_url = str(rec.get("cdn_url", "")).strip()
        if not cdn_url.lower().startswith("https://"):
            raise ValueError(
                f"records[{i}] ({rid}): cdn_url must be an https URL "
                f"(got {cdn_url!r}) — no file:// / placeholder is allowed"
            )
        status = rec.get("cdn_http_status")
        if status != 200:
            raise ValueError(
                f"records[{i}] ({rid}): cdn_http_status must be 200 "
                f"(got {status!r}) — the CDN URL must verify as a live 200"
            )
        entry = {
            "id": rid,
            "prompt": rec.get("prompt", ""),
            "file": rec.get("file", ""),
            "cdn_url": cdn_url,
            "cdn_http_status": 200,
            "used_on_page_id": rec.get("used_on_page_id", ""),
        }
        if "file_id" in rec:
            entry["file_id"] = rec["file_id"]
        out.append(entry)

    if out_path:
        Path(out_path).parent.mkdir(parents=True, exist_ok=True)
        with open(out_path, "w", encoding="utf-8") as f:
            json.dump(out, f, indent=2)
    return out


