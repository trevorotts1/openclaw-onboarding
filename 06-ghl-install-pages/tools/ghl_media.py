#!/usr/bin/env python3
"""ghl_media.py — T2 REAL IMAGE PIPELINE for the Skill-06 funnel/website builder.

WHAT THIS IS
------------
The build path the spec assumes is: the page copy drives REAL, on-brand raster
images (generated by the repo's verified KIE.ai tooling), those PNGs are uploaded
to the GoHighLevel media library (which returns a PUBLIC, login-free GCS object
URL), and the in-page custom-code element is rewritten to reference that CDN URL
via the proven token-id autosave (``ghl_rest_canvas.edit_element_customcode`` +
``page_autosave``). This module owns the deterministic, mechanical halves of that
pipeline so they are never improvised:

  1. ``build_prompts_json(copy_specs, ...)`` — mechanically derive the KIE
     ``prompts.json`` (one entry per image) from the page copy, with the
     mandatory English/Latin-only pin appended to EVERY prompt verbatim.
  2. ``generate_images(prompts_path, out_dir, ...)`` — a thin wrapper that shells
     the canonical ``kie_generate.py`` (REUSED AS-IS — never forked). It submits,
     polls, downloads, and PNG-magic-byte-verifies; this wrapper only locates the
     tool, runs it, and reports which ``<slide>.png`` files landed (+ re-asserts
     the PNG magic bytes itself, so a non-PNG can never be passed downstream).
  3. ``upload_media(png_path, location_id, name, pit, ...)`` — ports the PROVEN
     ``POST services.leadconnectorhq.com/medias/upload-file`` Bearer-PIT call from
     ``37-zhc-closeout/scripts/upload-ghl-media.sh`` to Python, returning
     ``{fileId, url}`` where ``url`` is the PUBLIC ``storage.googleapis.com/msgsndr/...``
     GCS object URL (openable with NO login — that is the real ``cdn_url``).
  4. ``image_tag(cdn_url, alt)`` + ``build_image_manifest(...)`` — the in-page
     ``<img>`` snippet and the ``images/manifest.json`` output contract.

AUTH-MODEL SPLIT (LOAD-BEARING — this module sits ENTIRELY on the bare-Python side)
-----------------------------------------------------------------------------------
  * KIE.ai (``api.kie.ai``) and the GHL MEDIA upload (``services.leadconnectorhq.com``
    + ``Authorization: Bearer <LOCATION PIT>``) are NOT behind the funnels-builder
    Cloudflare interstitial, so they run from bare Python. This module makes those
    calls directly (KIE via the reused subprocess; media via ``urllib``).
  * The IN-PAGE write that references the CDN URL (``/funnels/builder/autosave``)
    IS Cloudflare-WAF-gated (1010 from bare Python) and MUST run inside the
    agent-browser eval. This module does NOT make that call — it only produces the
    edited blob + the ``<img>`` snippet; ``ghl_rest_canvas.page_autosave`` (driven
    by ``ghl_builder.emit_rest_save_plan``) executes it in-browser.

KEYS — OPERATOR'S OWN, NEVER A CLIENT'S
---------------------------------------
``KIE_API_KEY`` is read by the reused ``kie_generate.py`` from the environment or
the standard env stores. The GHL LOCATION Private Integration Token (PIT) is read
by ``resolve_location_pit`` from EVERY known LOCATION-class env-var alias
(``GOHIGHLEVEL_API_KEY`` preferred → ``GHL_API_KEY`` → ``GOHIGHLEVEL_LOCATION_PIT``
→ ``GHL_LOCATION_PIT``) AND — when the live env is empty — the canonical env
STORES (``~/.openclaw/secrets/.env`` → ``~/clawd/secrets/.env`` →
``~/.openclaw/workspace/.env``), so a clean agent shell can never false-fail on a
token that physically lives in the store. It NEVER falls back to an AGENCY-class
PIT (``*_AGENCY_PIT``/``*_AGENCY_API_KEY``) — agency tokens 401 for media. The
location id is resolved the same way by ``resolve_location_id``
(``GOHIGHLEVEL_LOCATION_ID`` → ``GHL_LOCATION_ID`` → the ``*_ALLOWED_LOCATION_IDS``
single-id fallbacks → the same stores). In THIS phase the keys are the operator's
own fixture keys; a client key must never appear here.

NO-FABRICATION / FAIL-LOUD
--------------------------
Every step fails LOUD rather than substitute a placeholder: a non-PNG download
raises (never an SVG stub); a missing key raises (never a ``file://`` cdn_url); an
upload that returns no ``fileId``/``url`` raises (never a fake CDN URL). The
``images/manifest.json`` contract therefore can only ever carry real ``https``
``cdn_url`` values with a verified HTTP status.
"""

